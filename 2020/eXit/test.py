add = bytearray([0x0a, 0x09, 0x08, 0x07, 0x06, 0x05, 0x04, 0x03, 0x02, 0x01, 0x37, 0x13, 0x37, 0x13, 0xfe, 0xca, 0x37, 0x13, 0x37, 0x13, 0xfe, 0xca, 0xef, 0xbe, 0xad, 0xde])
xor = bytearray([0xde, 0xad, 0xbe, 0xef, 0xde, 0xad, 0xbe, 0xef, 0xca, 0xfe, 0x13, 0x37, 0xca, 0xfe, 0x13, 0x37, 0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0a])

def dec(mybytes):
    ret = bytearray(len(mybytes))
    for i, a in enumerate(mybytes):
        ret[i] = a - add[i] & 0xff
        ret[i] = ret[i] ^ xor[i]
    return ret

def enc(mystring):
    ret = bytearray(len(mystring))
    for i, a in enumerate(mystring):
        ret[i] = a ^ xor[i]
        ret[i] = ret[i] + add[i] & 0xff
    return ret


print(dec(enc(bytearray(b"Sit down next to my friend"))))
print(dec(enc(bytearray(b"Light match"))))
print(dec(enc(bytearray(b"Stay"))))
